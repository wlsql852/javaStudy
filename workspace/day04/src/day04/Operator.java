package day04;

public class Operator {
	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		double doubleB = 3;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		System.out.println(a/doubleB);
		System.out.println(a%doubleB);
		System.out.println(doubleB%b);
	}
}
