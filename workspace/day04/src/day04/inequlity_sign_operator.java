package day04;

public class inequlity_sign_operator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 30;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a+b<=c);
		System.out.println(a+b>=c);
		System.out.println(a<b);

	}

}
